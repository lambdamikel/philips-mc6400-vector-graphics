#!/usr/bin/env python3
"""Generate a rotating wireframe TORUS or SPHERE for the MC6400 (INS8070).

Geometry is precomputed here and baked as a vertex table; the CPU rotates +
projects (orthographic) each frame and streams an Eulerian-circuit route of the
mesh straight to the X/Y DAC -> one continuous stroke (clean on analog & DSO).
Velocity-based hex-pad control: 4/6 yaw, 2/8 pitch, 5 stop, 0 reset.

Usage:  gen_obj.py torus  asm/torus.asm
        gen_obj.py sphere asm/sphere.asm
"""
import math, sys
from collections import Counter

SINE = [round(64 * math.sin(2 * math.pi * i / 64)) for i in range(64)]


# ---------- geometry ----------
def torus(M=8, N=6, R=32, r=18):
    verts, edges = [], []
    for j in range(M):
        th = 2 * math.pi * j / M
        ct, st = math.cos(th), math.sin(th)
        for k in range(N):
            ph = 2 * math.pi * k / N
            rr = R + r * math.cos(ph)
            verts.append((round(rr * ct), round(r * math.sin(ph)), round(rr * st)))
    for j in range(M):
        for k in range(N):
            v = j * N + k
            edges.append((v, j * N + (k + 1) % N))         # minor ring
            edges.append((v, ((j + 1) % M) * N + k))       # major ring
    return verts, edges


def sphere(M=8, L=4, S=48):
    """M longitudes (even), L latitude rings between the poles."""
    verts, edges = [], []
    for i in range(L):
        beta = math.pi * (i + 1) / (L + 1) - math.pi / 2     # -90..+90
        y = S * math.sin(beta)
        rad = S * math.cos(beta)
        for k in range(M):
            a = 2 * math.pi * k / M
            verts.append((round(rad * math.cos(a)), round(y), round(rad * math.sin(a))))
    north = len(verts); verts.append((0, S, 0))
    south = len(verts); verts.append((0, -S, 0))
    for i in range(L):
        for k in range(M):
            v = i * M + k
            edges.append((v, i * M + (k + 1) % M))           # latitude ring
            if i < L - 1:
                edges.append((v, (i + 1) * M + k))           # vertical
    for k in range(M):
        edges.append((south, 0 * M + k))                     # south cap
        edges.append((north, (L - 1) * M + k))               # north cap
    return verts, edges


def eulerian(nv, edges):
    g = {x: Counter() for x in range(nv)}
    for a, b in edges:
        g[a][b] += 1; g[b][a] += 1
    odd = [x for x in range(nv) if sum(g[x].values()) % 2]
    if odd:
        raise SystemExit("graph not Eulerian (odd-degree vertices: %s)" % odd)
    start = next(x for x in range(nv) if sum(g[x].values()) > 0)
    stack, circ = [start], []
    while stack:
        v = stack[-1]
        if sum(g[v].values()) > 0:
            u = next(n for n in g[v] if g[v][n] > 0)
            g[v][u] -= 1; g[u][v] -= 1
            stack.append(u)
        else:
            circ.append(stack.pop())
    return circ


def db(vals):
    return ', '.join('0x%02X' % (v & 0xFF) for v in vals)


def emit(verts, route, title):
    vlines = "\n".join("        DB    " + db([x, y, z]) for (x, y, z) in verts)
    slines = "\n".join("        DB    " + db(SINE[o:o + 16]) for o in range(0, 64, 16))
    rlines = "\n".join("        DB    " + db(route[o:o + 16]) for o in range(0, len(route), 16))
    return TEMPLATE.format(NVERT=len(verts), NROUTE=len(route), TITLE=title,
                           SINE=slines, VERTS=vlines, ROUTE=rlines)


