if __name__ == "__main__":
    kind = sys.argv[1] if len(sys.argv) > 1 else "torus"
    out = sys.argv[2] if len(sys.argv) > 2 else "asm/%s.asm" % kind
    if kind == "torus":
        verts, edges = torus()
        title = "ROTATING WIREFRAME TORUS"
    elif kind == "sphere":
        verts, edges = sphere()
        title = "ROTATING WIREFRAME SPHERE"
    else:
        raise SystemExit("kind must be torus or sphere")
    route = eulerian(len(verts), edges)
    open(out, "w").write(emit(verts, route, title))
    print("wrote %s  (%s: %d verts, %d edges, route %d)" %
          (out, kind, len(verts), len(edges), len(route)))
